<?php

namespace App\Controller;

use Psr\Log\LoggerInterface;
use Sensio\Bundle\FrameworkExtraBundle\Configuration\Route;
use Symfony\Bundle\FrameworkBundle\Controller\AbstractController;
use Symfony\Component\HttpFoundation\JsonResponse;
use Symfony\Component\HttpFoundation\Response;
use Twig\Environment;

class ArticleController extends AbstractController
{
    /**
     * @Route("/", name="app_homepage")
     */
    public function homepage()
    {
        return $this->render('game/homepage.html.twig');
    }

    /**
     * @Route("/news/{slug}", name="article_show")
     */
    public function show($slug)
    {
        $comments = [
            'Très bon jeu (que ce soit en terme graphique ou gameplay) et avec de très bonne ost.
            Toutefois, attention si vous prenez l extension "Iceborne" puisqu elle est plus gourmande en ressource et peux engendrer des crash faisant perdre la connexion à la session à la mort d un joueur',
            'Monstres. Chasser. Être stylé.
            Point.',
            'Très bon jeux déjà 900 h je le recommande',
        ];

        return $this->render('game/show.html.twig', [
            'title' => ucwords(str_replace('-', ' ', $slug)),
            'slug' => $slug,
            'comments' => $comments,
        ]);
    }

